def slugify(text: str) -> str:
    """Turn arbitrary text into a safe branch-name / filename segment."""
    import re
    text = text.lower()
    text = re.sub(r"[^a-z0-9]+", "-", text)
    return text.strip("-")[:48]


def ensure_label(repo, name: str, color: str, description: str = "") -> None:
    """Create the GitHub label if it doesn't already exist."""
    try:
        repo.get_label(name)
    except GithubException:
        try:
            repo.create_label(name=name, color=color, description=description)
            safe_print(f"[INFO] Created GitHub label: '{name}'")
        except GithubException as exc:
            safe_print(f"[WARN] Could not create label '{name}': {exc}")


