def run_auto_coder(alerts: list[dict], mock: bool = False) -> None:
    """Process every Opportunity alert that has a target_entity."""
    candidates = [
        a for a in alerts
        if a.get("type", "").lower() == "opportunity"
        and a.get("target_entity")
    ]

    if not candidates:
        safe_print("[INFO] No actionable Opportunity alerts with a target_entity. Nothing to do.")
        return

    mode_label = "MOCK (template)" if mock else "Gemini AI"
    safe_print(f"\n[RADAR] Auto-Coder starting — {len(candidates)} candidate(s) | mode: {mode_label}\n")
    pr_urls = []

    for i, alert in enumerate(candidates, start=1):
        entity = alert.get("target_entity", "unknown")
        safe_print(f"{'─'*50}")
        safe_print(f"[{i}/{len(candidates)}] Target entity : {entity}")
        safe_print(f"            Paper         : {alert.get('paper_title', '?')}")

        try:
            # Step A — generate code (Gemini or local template)
            if mock:
                safe_print("[MOCK]   Generating template code locally (no API call)...")
                code = generate_mock_code(alert)
                safe_print(f"[MOCK]   Code generated ({len(code)} chars).")
            else:
                code = generate_code_with_gemini(alert)
                safe_print(f"[GEMINI] Code generated ({len(code)} chars).")

            # Step B — commit + PR
            pr_url = create_pull_request(GITHUB_TOKEN, GITHUB_ORG, GITHUB_REPO, alert, code)
            if pr_url:
                pr_urls.append(pr_url)

        except Exception as exc:
            safe_print(f"[ERROR] Failed for '{entity}': {exc}")
            continue

    safe_print(f"\n{'═'*50}")
    safe_print(f"[DONE] {len(pr_urls)} Pull Request(s) created:")
    for url in pr_urls:
        safe_print(f"       → {url}")
    safe_print(f"{'═'*50}\n")


