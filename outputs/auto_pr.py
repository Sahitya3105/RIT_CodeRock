def build_gemini_prompt(alert: dict) -> str:
    """
    Construct a detailed prompt so Gemini writes a *real* Python implementation
    of the algorithm described in the ArXiv paper referenced by the alert.
    """
    paper_title   = alert.get("paper_title",   "Unknown Paper")
    url           = alert.get("url",            "")
    reason        = alert.get("reason",         "")
    action        = alert.get("action",         "")
    target_entity = alert.get("target_entity",  "improved_algorithm")
    entity_type   = alert.get("entity_type",    "function")

    prompt = textwrap.dedent(f"""
    You are an expert AI research engineer working at Samsung R&D.

    An autonomous research-monitoring agent called RADAR has flagged the following
    ArXiv paper as a high-priority opportunity for Samsung's on-device AI stack:

    PAPER TITLE : {paper_title}
    PAPER URL   : {url}
    WHY IT MATTERS:
    {reason}

    RECOMMENDED ACTION:
    {action}

    YOUR TASK
    ---------
    Write a complete, production-quality Python implementation of the core algorithm
    described in the paper above.  The implementation must:

    1. Target the entity named  `{target_entity}` (entity type: {entity_type}).
    2. Be fully self-contained — include all imports at the top.
    3. Include a detailed module-level docstring that:
       - Names the source paper and its URL
       - Summarises the mathematical idea in plain English
       - Lists any key hyperparameters and their default values
    4. Add NumPy-style docstrings to every public function / class.
    5. Include at least one concrete usage example inside an
       `if __name__ == "__main__":` block that can be run immediately.
    6. Prefer PyTorch (torch) for tensor operations; fall back to NumPy if simpler.
    7. Add inline comments explaining the mathematics / algorithm steps.
    8. Do NOT include placeholder stubs — every function must be fully implemented.

    Return ONLY the raw Python source code.  Do NOT wrap it in markdown fences.
    Do NOT add any explanation before or after the code.
    """).strip()

    return prompt


def generate_code_with_gemini(alert: dict) -> str:
    """
    Call Gemini via the google.genai SDK and return generated Python source.
    Falls back to Groq, then OpenRouter (free) if Gemini is unavailable.
    """
    import urllib.request
    prompt = build_gemini_prompt(alert)
    safe_print(f"[AI] Generating code for: {alert.get('target_entity', '?')} ...")

    # 1. Try Gemini
    if GOOGLE_API_KEY:
        try:
            client = genai.Client(api_key=GOOGLE_API_KEY)
            safe_print(f"[GEMINI] Trying {GEMINI_MODEL}...")
            response = client.models.generate_content(
                model=GEMINI_MODEL,
                contents=prompt,
                config=genai_types.GenerateContentConfig(
                    temperature=0.2,
                    max_output_tokens=8192,
                ),
            )
            return strip_markdown_fences(response.text.strip())
        except Exception as exc:
            safe_print(f"[WARN] Gemini failed: {exc}")

    # 2. Try Groq
    groq_api_key = os.getenv("GROQ_API_KEY")
    if groq_api_key:
        try:
            from groq import Groq
            client = Groq(api_key=groq_api_key)
            safe_print("[GROQ] Switching to Llama-3...")
            chat_completion = client.chat.completions.create(
                messages=[{"role": "user", "content": prompt}],
                model="llama-3.3-70b-versatile",
            )
            return strip_markdown_fences(chat_completion.choices[0].message.content.strip())
        except Exception as groq_err:
            safe_print(f"[WARN] Groq fallback failed: {groq_err}")

    # 3. Try OpenRouter (free tier — no daily limit)
    openrouter_key = os.getenv("OPENROUTER_API_KEY")
    if openrouter_key:
        free_models = [
            "google/gemma-4-31b-it:free",
            "nvidia/nemotron-3-nano-omni-30b-a3b-reasoning:free",
            "nvidia/nemotron-3-super-120b-a12b:free",
        ]
        or_headers = {
            "Content-Type": "application/json",
            "Authorization": f"Bearer {openrouter_key}",
            "HTTP-Referer": "https://github.com/Samsung-PRISM-EdgeAI",
            "X-Title": "RADAR Auto-Coder"
        }
        for model in free_models:
            try:
                safe_print(f"[OPENROUTER] Trying {model.split('/')[1]}...")
                payload = json.dumps({
                    "model": model,
                    "messages": [{"role": "user", "content": prompt}]
                }).encode()
                req = urllib.request.Request(
                    "https://openrouter.ai/api/v1/chat/completions",
                    data=payload, headers=or_headers
                )
                r = urllib.request.urlopen(req, timeout=90)
                resp = json.loads(r.read())
                code = resp["choices"][0]["message"]["content"].strip()
                safe_print(f"[OPENROUTER] Success with {model.split('/')[1]}")
                return strip_markdown_fences(code)
            except Exception as e:
                safe_print(f"[WARN] OpenRouter {model.split('/')[1]} failed: {str(e)[:80]}")
                time.sleep(3)

    # 4. Final fallback — generate rich local template
    safe_print("[INFO] All AI providers unavailable — using rich local template.")
    return generate_mock_code(alert)


def strip_markdown_fences(code: str) -> str:
    """Strip accidental markdown fences AI models sometimes add."""
    if code.startswith("```"):
        lines = code.splitlines()
        code = "\n".join(lines[1:-1] if lines[-1].strip() == "```" else lines[1:])
    return code

    return code


