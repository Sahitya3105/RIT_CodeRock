def find_best_repo(g, org_name, target_entity, fallback_repo=None):
    """Identical to dispatcher logic: find the best repo for the entity."""
    if not org_name:
        if fallback_repo:
            return g.get_repo(fallback_repo)
        return None

    try:
        org = g.get_organization(org_name)
        repos = org.get_repos()
        target_entity_clean = target_entity.lower().replace(" ", "-")
        for repo in repos:
            if target_entity_clean in repo.name.lower():
                return repo
        if fallback_repo:
            return g.get_repo(fallback_repo)
        return None
    except Exception as e:
        safe_print(f"[ERROR] Could not query organization '{org_name}': {e}")
        return None

def create_pull_request(token: str, org_name: str, fallback_repo: str, alert: dict, code: str) -> str:
    """
    1. Find target repo in Org
    2. Get default branch SHA
    3. Create feature branch
    4. Commit generated code
    5. Open PR
    """
    g    = Github(token)
    
    target_entity = alert.get("target_entity", "algorithm")
    repo = find_best_repo(g, org_name, target_entity, fallback_repo)

    if not repo:
        safe_print(f"[WARN] Skipping PR: Could not find target repo for entity '{target_entity}'")
        return None

    safe_print(f"[INFO] Routing PR for '{target_entity}' to repo: {repo.full_name}")

    slug          = slugify(target_entity)
    timestamp     = datetime.now(timezone.utc).strftime("%Y%m%d-%H%M%S")
    branch_name   = f"{BRANCH_PREFIX}/{slug}-{timestamp}"
    
    # Route code to the correct Samsung department folder
    dept = alert.get("department", "").strip()
    if dept:
        file_path = f"data/{dept}/{slug}.py"
    else:
        file_path = f"{GENERATED_CODE_DIR}/{slug}.py"

    generated_at  = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    # ── 1. Get base branch SHA ───────────────────────────────────────────────
    default_branch = repo.default_branch
    base_sha       = repo.get_branch(default_branch).commit.sha
    safe_print(f"[GH]    Base branch: {default_branch} @ {base_sha[:8]}")

    # ── 2. Create feature branch ─────────────────────────────────────────────
    try:
        repo.create_git_ref(ref=f"refs/heads/{branch_name}", sha=base_sha)
        safe_print(f"[GH]    Created branch: {branch_name}")
    except GithubException as exc:
        safe_print(f"[ERROR] Could not create branch '{branch_name}': {exc}")
        raise

    # ── 3. Commit the generated file ─────────────────────────────────────────
    commit_message = (
        f"feat(auto-coder): implement {target_entity} from ArXiv paper\n\n"
        f"Auto-generated by RADAR Auto-Coder on {generated_at}\n"
        f"Source: {alert.get('url', '')}"
    )

    # Check if file already exists on the branch (for update vs create)
    try:
        existing = repo.get_contents(file_path, ref=branch_name)
        repo.update_file(
            path=file_path,
            message=commit_message,
            content=code,
            sha=existing.sha,
            branch=branch_name,
        )
        safe_print(f"[GH]    Updated file: {file_path}")
    except GithubException:
        repo.create_file(
            path=file_path,
            message=commit_message,
            content=code,
            branch=branch_name,
        )
        safe_print(f"[GH]    Created file: {file_path}")

    # ── 4. Ensure label exists ───────────────────────────────────────────────
    ensure_label(
        repo, PR_LABEL_NAME, PR_LABEL_COLOR,
        "Autonomous PR created by RADAR Auto-Coder"
    )

    # ── 5. Open the Pull Request ─────────────────────────────────────────────
    pr_title = f"[RADAR Auto-Coder] Implement {target_entity} from ArXiv"
    pr_body  = build_pr_body(alert, branch_name, file_path, generated_at)

    try:
        label_obj = repo.get_label(PR_LABEL_NAME)
        pr = repo.create_pull(
            title=pr_title,
            body=pr_body,
            head=branch_name,
            base=default_branch,
        )
        pr.add_to_labels(label_obj)
        safe_print(f"[OK]    PR opened: #{pr.number} → {pr.html_url}")
        return pr.html_url
    except GithubException as exc:
        safe_print(f"[ERROR] Could not open PR: {exc}")
        raise


