def generate_mock_code(alert: dict) -> str:
    """
    Build a complete, runnable Python implementation template based on the
    alert metadata alone.  Used when --mock is passed or Gemini is unavailable.
    """
    paper_title   = alert.get("paper_title",   "Unknown Paper")
    url           = alert.get("url",            "")
    reason        = alert.get("reason",         "")
    action        = alert.get("action",         "")
    target_entity = alert.get("target_entity",  "algorithm")
    generated_at  = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")

    # Derive a clean class/function name
    func_name = re.sub(r"[^a-zA-Z0-9_]", "_", target_entity).strip("_")

    return textwrap.dedent(f'''
    """
    Auto-generated by RADAR Auto-Coder
    Generated : {generated_at}

    Source Paper
    ------------
    Title : {paper_title}
    URL   : {url}

    Why This Matters
    ----------------
    {reason}

    Recommended Action
    ------------------
    {action}
    """

    import math
    from typing import Optional

    import numpy as np

    try:
        import torch
        import torch.nn as nn
        _TORCH_AVAILABLE = True
    except ImportError:
        _TORCH_AVAILABLE = False


    # ---------------------------------------------------------------------------
    # Core implementation
    # ---------------------------------------------------------------------------

    def {func_name}(
        weights: "np.ndarray | torch.Tensor",
        bits: int = 4,
        group_size: int = 128,
        symmetric: bool = True,
        clamp_outliers: bool = True,
        outlier_threshold: float = 3.0,
    ):
        """
        Implement the algorithm described in: {paper_title}

        Parameters
        ----------
        weights : np.ndarray or torch.Tensor
            The weight matrix to process.
        bits : int
            Target bit-width (default: 4).
        group_size : int
            Number of elements per quantisation group (default: 128).
        symmetric : bool
            Use symmetric quantisation around zero (default: True).
        clamp_outliers : bool
            Clamp statistical outliers before quantising (default: True).
        outlier_threshold : float
            Std-dev multiplier for outlier detection (default: 3.0).

        Returns
        -------
        Same type as input — quantised weights dequantised back to float32.
        """
        use_torch = _TORCH_AVAILABLE and hasattr(weights, "dim")

        if use_torch:
            w = weights.float().cpu()
        else:
            w = np.asarray(weights, dtype=np.float32)

        # --- Step 1: Reshape into groups -----------------------------------
        original_shape = w.shape if use_torch else w.shape
        flat = w.reshape(-1) if use_torch else w.ravel()
        n = flat.shape[0] if use_torch else flat.size
        pad = (group_size - n % group_size) % group_size

        if pad:
            zeros = (torch.zeros(pad) if use_torch else np.zeros(pad, dtype=np.float32))
            flat = (torch.cat([flat, zeros]) if use_torch else np.concatenate([flat, zeros]))

        groups = flat.reshape(-1, group_size)

        # --- Step 2: Optional outlier clamping -----------------------------
        if clamp_outliers:
            if use_torch:
                mu, sigma = groups.mean(dim=1, keepdim=True), groups.std(dim=1, keepdim=True)
            else:
                mu, sigma = groups.mean(axis=1, keepdims=True), groups.std(axis=1, keepdims=True)
            lo, hi = mu - outlier_threshold * sigma, mu + outlier_threshold * sigma
            groups = (torch.clamp(groups, lo, hi) if use_torch
                      else np.clip(groups, lo, hi))

        # --- Step 3: Compute scale (and zero-point for asymmetric) ---------
        qmin = -(2 ** (bits - 1))       # e.g. -8  for 4-bit
        qmax =  (2 ** (bits - 1)) - 1   # e.g.  7  for 4-bit

        if symmetric:
            if use_torch:
                abs_max = groups.abs().max(dim=1, keepdim=True).values
            else:
                abs_max = np.abs(groups).max(axis=1, keepdims=True)
            scale = abs_max / qmax
            scale = (torch.clamp(scale, min=1e-8) if use_torch
                     else np.maximum(scale, 1e-8))
            zero_point = 0
        else:
            if use_torch:
                g_min = groups.min(dim=1, keepdim=True).values
                g_max = groups.max(dim=1, keepdim=True).values
            else:
                g_min = groups.min(axis=1, keepdims=True)
                g_max = groups.max(axis=1, keepdims=True)
            scale = (g_max - g_min) / (qmax - qmin)
            scale = (torch.clamp(scale, min=1e-8) if use_torch
                     else np.maximum(scale, 1e-8))
            zero_point = qmin - g_min / scale

        # --- Step 4: Quantise and dequantise (simulated quantisation) ------
        q = groups / scale + (0 if symmetric else zero_point)
        q = (torch.clamp(q.round(), qmin, qmax) if use_torch
             else np.clip(np.round(q), qmin, qmax))
        dq = (q - (0 if symmetric else zero_point)) * scale

        # --- Step 5: Restore original shape --------------------------------
        result = dq.reshape(-1)[:n]
        result = result.reshape(original_shape)
        return result if use_torch else result


    # ---------------------------------------------------------------------------
    # Demo
    # ---------------------------------------------------------------------------

    if __name__ == "__main__":
        print("RADAR Auto-Coder — {func_name} demo")
        print("Source : {url}")
        print()

        # Synthetic weight matrix (simulates a transformer layer)
        rng = np.random.default_rng(42)
        W = rng.standard_normal((128, 256)).astype(np.float32)
        print(f"Original  | mean={{W.mean():.4f}}  std={{W.std():.4f}}  range=[{{W.min():.3f}}, {{W.max():.3f}}]")

        W_q = {func_name}(W, bits=4, group_size=64, symmetric=True)
        error = np.abs(W - W_q).mean()
        print(f"Quantised | mean={{W_q.mean():.4f}}  std={{W_q.std():.4f}}  MAE={{error:.6f}}")
        print(f"\nMemory reduction: {{(1 - 4/32)*100:.0f}}% (32-bit → 4-bit)")
    ''').lstrip()


